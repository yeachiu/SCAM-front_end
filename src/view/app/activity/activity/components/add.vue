<template>
  <div>
    <Modal v-model="show" title="添加活动"
            :mask-closable="false" width="900">
    <!-- <Card :bordered="false" dis-hover> -->
      <p slot="title">
        <Steps :current="current" style="margin-left: 10%;">
          <Step title="基本信息" icon="ios-person"></Step>
          <Step title="报名表单" icon="ios-camera"></Step>
          <Step title="学分配置" icon="ios-mail"></Step>
      </Steps>
      </p>   
      <Card :bordered="false" v-show = "current == 0"  dis-hover>
        <BaseForm ref="baseForm" :aparId="aparId" :user="user" v-on:submitData="cacheBaseData" class="TabContent"/>
      </Card>
      <Card :bordered="false" v-show = "current == 1"  dis-hover>
        <SignupForm ref="signupForm" v-on:submitData="cacheSignupData" class="TabContent"/>
      </Card>
      <Card :bordered="false" v-show = "current == 2"  dis-hover style="min-height:400px;">
        <ScoreSettingForm ref="scoreForm" v-on:submitData="cacheScoreData" class="TabContent"/>
      </Card>
      <div id="button-group">
        <template v-if="current == 2">
          <Button type="primary" @click="prev">上一步</Button>
        </template>
        <template v-else>
          <Button type="primary" @click="prev">上一步</Button>
          <Button type="primary" @click="next">下一步</Button>
        </template>
      </div>
      <div slot="footer">
        <Button type="default" :disabled="loading" @click="cancel(false)">取消</Button>
        <Button type="primary" @click="save(1)">存为草稿</Button>
        <Button type="primary" @click="save(2)">保存发布</Button>
      </div>
    <!-- </Card> -->
    </Modal>
  </div>
</template>
<script>
import { post } from '@/libs/axios-cfg'
import axios from 'axios';
import qs from 'qs'
import BaseForm from '@/view/app/activity/activity/components/base-form.vue'
import SignupForm from '@/view/app/activity/activity/components/signup-form.vue'
import ScoreSettingForm from '@/view/app/activity/activity/components/score-setting-form.vue'
export default {
  data () {
    return {
      dictId: '',
      show: true,
      loading:false,
      current: 0,
      baseDatas:{},
      scoreData:{},
      finalDatas:{
        organizerId:'',
        pictureUrl: '',
        title: '',
        description: '',
        address:'',
        signupTime: {},
        deadlineTime:{},
        startTime:{},
        endTime:{},
        limitQuota: 100,
        otherAdmin: '',
        isblacklist: 1,
        isreview: 1,
        groupId: '',
        status:0,
        rules:'',
        scoreData:''
      },
      apartmentId:'',
      user:null
    }
  },
  components:{
    BaseForm,SignupForm,ScoreSettingForm
  },
  created(){
    this.aparId = this.$store.state.user.aparId;
  },
  methods: {
    /**
     * @description 关闭Modal
     * @param reload 是否重新加载数据
     */
    cancel(reload = false) {
      this.$emit("cancel", "add", reload);
    },
    next () {
      if (this.current == 2) {
        this.current = 0;
      } else {
        this.current += 1;
      }
    },
    prev () {
      if (this.current == 0) {
        this.current = 0;
      } else {
        this.current -= 1;
      }
    },
    async save(type){
      //从子组件获取表单数据
      this.$refs.baseForm.$emit("submitBaseData");
      this.$refs.signupForm.$emit("submitSignupData");
      this.$refs.scoreForm.$emit("submitScoreData");

      this.finalDatas.status = type;
      this.finalDatas.organizerId = this.aparId;
      try {
        await post('/app/activity/add',this.finalDatas)
        this.$Message.destroy()
        this.$Message.success({
            content:"活动添加成功",
            duration: 1.5
        });
        this.cancel(false);
      } catch (error) {
        this.$throw(error)
      }
    },
    cacheBaseData(val){
      // this.finalDatas.baseData = val;
      let baseDatas = val;
      if(baseDatas.otherAdmin != null && baseDatas.otherAdmin.length > 0){
        this.finalDatas.otherAdmin = String(baseDatas.otherAdmin);
      }
      if(baseDatas.groupId != null && baseDatas.groupId.length > 0){
        let ids = new Array();
        // 拼接分组ID
        baseDatas.groupId.forEach(ele => {
          if(ele.id != null){
            ids.push(ele.id);
          }
        });
        this.finalDatas.groupId = String(ids);
      }
      this.finalDatas.title = baseDatas.title;
      this.finalDatas.description = baseDatas.description;
      this.finalDatas.address = baseDatas.address;
      this.finalDatas.pictureUrl = baseDatas.pictureUrl;
      this.finalDatas.signupTime = new Date(baseDatas.datetimeSignup[0]);
      this.finalDatas.deadlineTime = new Date(baseDatas.datetimeSignup[1]);
      this.finalDatas.startTime = new Date(baseDatas.datetimeAct[0]);
      this.finalDatas.endTime = new Date(baseDatas.datetimeAct[1]);
      this.finalDatas.limitQuota = baseDatas.limitQuota;
      this.finalDatas.isblacklist = new Number(baseDatas.isblacklist);
      this.finalDatas.isreview = new Number(baseDatas.isreview); 
    },
    cacheSignupData(val){
      this.finalDatas.rules = JSON.stringify(val);
    }, 
    cacheScoreData(val){
      this.finalDatas.scoreData = JSON.stringify(val);
    }    
  }
}
</script>

